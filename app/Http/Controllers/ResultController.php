<?php

namespace App\Http\Controllers;

use App\Models\CandidateExamAttempt;
use App\Models\CandidatePerformanceProfile;
use App\Models\Exam;
use App\Models\User;
use App\Services\ResultManagementService;
use Illuminate\Database\Eloquent\Builder;
use Illuminate\Http\Request;
use Illuminate\Support\Facades\Response;
use Illuminate\Validation\ValidationException;
use Inertia\Inertia;
use Inertia\Response as InertiaResponse;

class ResultController extends Controller
{
    public function __construct(private readonly ResultManagementService $results)
    {
    }

    public function index(Request $request): InertiaResponse
    {
        $exams = $this->examScope($request->user(), $request->route('organization'))
            ->withCount(['attempts as submitted_attempts_count' => fn ($query) => $query->whereIn('status', [
                CandidateExamAttempt::STATUS_SUBMITTED,
                CandidateExamAttempt::STATUS_AUTO_SUBMITTED,
            ])])
            ->latest()
            ->get()
            ->map(fn (Exam $exam) => [
                'id' => $exam->id,
                'title' => $exam->title,
                'exam_code' => $exam->code,
                'status' => $exam->status,
                'submitted_attempts_count' => $exam->submitted_attempts_count,
                'total_marks' => $exam->total_marks,
                'pass_mark' => $exam->pass_mark,
            ]);

        $attempts = CandidateExamAttempt::query()
            ->whereIn('exam_id', $exams->pluck('id'))
            ->whereIn('status', [CandidateExamAttempt::STATUS_SUBMITTED, CandidateExamAttempt::STATUS_AUTO_SUBMITTED])
            ->with(['candidate', 'exam', 'answers.subject', 'proctoringEvents'])
            ->get();

        return Inertia::render('Results/Index', [
            'exams' => $exams,
            'dashboard' => $this->results->dashboard($attempts),
        ]);
    }

    public function show(Request $request, Exam $exam): InertiaResponse
    {
        $this->authorizeExam($request->user(), $exam);
        $attempts = $this->results->queryForExam($exam)->get();
        $rows = $attempts->map(fn (CandidateExamAttempt $attempt) => $this->results->row($attempt))->values();

        return Inertia::render('Results/Exam', [
            'exam' => [
                'id' => $exam->id,
                'title' => $exam->title,
                'exam_code' => $exam->code,
                'total_marks' => $exam->total_marks,
                'pass_mark' => $exam->pass_mark,
            ],
            'rows' => $rows,
            'dashboard' => $this->results->dashboard($attempts),
            'adaptive_analysis' => $this->examAdaptiveAnalysis($exam),
        ]);
    }

    public function candidate(Request $request, CandidateExamAttempt $attempt): InertiaResponse
    {
        $attempt->loadMissing(['exam', 'candidate', 'answers.question.options', 'answers.subject', 'papers.question.options', 'papers.question.subject', 'papers.question.questionBank', 'proctoringEvents']);
        $this->authorizeExam($request->user(), $attempt->exam);

        return Inertia::render('Results/Candidate', [
            'result' => $this->results->row($attempt),
            'answers' => $this->markedPaperRows($attempt, $request->user()),
            'adaptive' => $this->candidateAdaptiveAnalysis($attempt),
        ]);
    }

    public function exportCsv(Request $request, Exam $exam)
    {
        $this->authorizeExam($request->user(), $exam);
        $rows = $this->results->queryForExam($exam)->get()->map(fn (CandidateExamAttempt $attempt) => $this->results->row($attempt));

        return Response::make($this->results->csv($rows), 200, [
            'Content-Type' => 'text/csv',
            'Content-Disposition' => 'attachment; filename="'.$exam->code.'-results.csv"',
        ]);
    }

    public function exportPdf(Request $request, Exam $exam)
    {
        $this->authorizeExam($request->user(), $exam);
        $exam->loadMissing(['organization', 'school', 'center']);
        $attempts = $this->results->queryForExam($exam)->get();
        $dashboard = $this->results->dashboard($attempts);
        $rows = $attempts->map(fn (CandidateExamAttempt $attempt) => $this->results->row($attempt))->values();
        $lines = [
            'AlignEx Examination Result Summary',
            'Organization: '.($exam->organization?->name ?? 'N/A'),
            'School: '.($exam->school?->name ?? 'N/A'),
            'Center: '.($exam->center?->name ?? 'N/A'),
            'Generated By: '.$request->user()->name.' ('.$request->user()->email.')',
            'Exam: '.$exam->title,
            'Code: '.$exam->code,
            'Submitted Candidates: '.$dashboard['summary']['total'],
            'Passed: '.$dashboard['summary']['passed'],
            'Failed: '.$dashboard['summary']['failed'],
            'Average Percentage: '.$dashboard['summary']['average_percentage'].'%',
            'Generated: '.now()->toDateTimeString(),
            '',
            'Candidate Performance Table',
            sprintf('%-24s %-16s %8s %8s %6s %-8s %-10s %s', 'Candidate', 'Reg. No.', 'Score', 'Percent', 'Grade', 'Status', 'Duration', 'Hash'),
            str_repeat('-', 112),
        ];

        foreach ($rows as $row) {
            $lines[] = sprintf(
                '%-24s %-16s %8s %7s%% %6s %-8s %-10s %s',
                str($row['candidate_name'])->limit(24, '')->toString(),
                str($row['registration_number'])->limit(16, '')->toString(),
                $row['score'].'/'.$row['total_marks'],
                $row['percentage'],
                $row['grade'],
                $row['status'],
                $row['duration_used'],
                $row['result_hash']
            );
        }

        return Response::make($this->results->pdf('AlignEx Result Export', $lines), 200, [
            'Content-Type' => 'application/pdf',
            'Content-Disposition' => 'attachment; filename="'.$exam->code.'-result-summary.pdf"',
        ]);
    }

    public function markedPaperPdf(Request $request, CandidateExamAttempt $attempt)
    {
        $attempt->loadMissing(['exam.organization', 'exam.school', 'exam.center', 'candidate', 'answers.question.options', 'answers.subject', 'papers.question.options', 'papers.question.subject', 'papers.question.questionBank', 'proctoringEvents']);
        $this->authorizeExam($request->user(), $attempt->exam);

        abort_unless(in_array($attempt->status, [CandidateExamAttempt::STATUS_SUBMITTED, CandidateExamAttempt::STATUS_AUTO_SUBMITTED], true), 404);

        $result = $this->results->row($attempt);
        $lines = [
            'AlignEx Detailed Marked Question Paper',
            'Organization: '.($attempt->exam?->organization?->name ?? 'N/A'),
            'School: '.($attempt->exam?->school?->name ?? 'N/A'),
            'Center: '.($attempt->exam?->center?->name ?? 'N/A'),
            'Generated By: '.$request->user()->name.' ('.$request->user()->email.')',
            'Exam: '.$attempt->exam?->title.' ('.$attempt->exam?->code.')',
            'Candidate: '.trim(($attempt->candidate?->first_name ?? '').' '.($attempt->candidate?->last_name ?? '')),
            'Registration Number: '.$attempt->candidate?->candidate_number,
            'Score: '.$result['score'].'/'.$result['total_marks'].' | '.$result['percentage'].'% | '.$result['status'],
            'Submitted At: '.($attempt->submitted_at?->toDateTimeString() ?? 'N/A'),
            'Verification Hash: '.$result['result_hash'],
            '',
        ];

        foreach ($this->markedPaperRows($attempt, $request->user()) as $index => $row) {
            $lines[] = 'Q'.($index + 1).' ['.$row['subject'].'] Score: '.($row['score_awarded'] ?? '0.00').'/'.$row['marks'];
            $lines[] = 'Question: '.$row['question'];
            foreach ($row['options'] as $option) {
                $markers = [];
                if ($option['selected']) {
                    $markers[] = 'Selected';
                }
                if ($option['correct']) {
                    $markers[] = 'Correct';
                }
                $lines[] = '  '.$option['label'].'. '.$option['option_text'].($markers === [] ? '' : ' ['.implode(', ', $markers).']');
            }
            $lines[] = 'Candidate Answer: '.($row['selected_labels'] ?: 'Not answered');
            $lines[] = 'Correct Answer: '.($row['correct_labels'] ?: 'N/A');
            if ($row['explanation']) {
                $lines[] = 'Explanation: '.$row['explanation'];
            }
            $lines[] = '';
        }

        return Response::make($this->results->pdf('AlignEx Marked Paper', $lines), 200, [
            'Content-Type' => 'application/pdf',
            'Content-Disposition' => 'attachment; filename="'.$attempt->candidate?->candidate_number.'-'.$attempt->exam?->code.'-marked-paper.pdf"',
        ]);
    }

